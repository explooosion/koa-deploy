'use strict';

export default function checkauth() {
    return async(ctx, next) => {

        if (1 == 1 ||
            ctx.path === '/' ||
            ctx.path.indexOf('/api') >= 0 ||
            ctx.path.indexOf('/about') >= 0
        ) {
            await next()
        } else {
            ctx.throw(401);
        }

    }
}